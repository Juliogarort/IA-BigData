# Crea una lista de nombres y utiliza count() para contar cuántas veces aparece un nombre específico.

nombres = ["Oc", "José", "Joseca", "Oc", "Paco", "Pepe", "Oc"]
contador_nombre = "Oc"
conteo = nombres.count(contador_nombre)
print(f"El nombre '{contador_nombre}' aparece {conteo} veces en la lista.")
